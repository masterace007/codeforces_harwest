void solve(){
	int n,m;
	cin >> n >> m;
	if(n == m)
		cout<<0<<endl;
	else{
		int temp = n;
		int count = 0;
		while(temp <= m){
			count++;
			temp *= 2;
		}
		temp = n;
		int count_3 = 0;
		while(temp <= m){
			count_3++;
			temp *= 3;
		}
		for(int i = 0; i < count; ++i){
			for(int j = 0; j < count_3; ++j) {
				temp = n * pow(2, i) * pow(3, j);
				if(temp == m) {
					cout << i + j << endl;
					return;
				}
			}
		}
		cout<<-1<<endl;
	}
}

